#include <stdio.h>

int main() {
    char a[5][5] = {{' ', ' ', '1', ' ', ' '},
                    {' ', '1', '2', '1', ' '},
                    {'1', '2', '3', '2', '1'},
                    {' ', '1', '2', '1', ' '},
                    {' ', ' ', '1', ' ', ' '}};

    int i, j;
    for (i = 0; i < 5; i++) {
        printf("\n");
        for (j = 0; j < 5; j++) {
            printf("%c", a[i][j]);
        }
    }
    printf("\n");
}